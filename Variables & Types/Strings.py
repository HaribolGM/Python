#Strings

# Strings are defined either with a single quote or a double quote.

# mystring = 'hello'
# print(mystring)
# mystring = "Hello"
# print(mystring)

# The difference between the two is that using double quotes
# makes it easy to include apostrophes 

# mystring = "Don't worry about apostrophes"
# print(mystring)



# Simple operators can be executed on numbers and strings:

# one  = 1
# two  = 2 
# three = one + two
# print(three)

# hello = "Hello"
# world = "world"
# helloworld = hello + " " + world
# print(helloworld)

# Assignments can be done on more than one variable
# "simultaneously" on the same line like this:

# a, b = 3, 4
# print(a,b)


# Mixing operators between numbers and strings is not supported:

# This will not work!
# one = 1 
# two = 2 
# hello = "Hello"

# print(one + two +  hello)

